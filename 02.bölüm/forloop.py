# for döngüleri
print("basit for döngüsü")
for i in range(5):
    print(i)

print("başlangıç değerli")
for i in range(3,5):
    print(i)

print("tam for döngüsü (başlangıç, bitiş, artım)")
for i in range (2,7,2):
    print(i)

print("ters for döngüsü")
for i in range (7,2,-2):
    print(i)

list = ["ali", "veli", "ayse" "zeynep", "hatice"]
print("listeleme döngüsü")
for l in list:
    print(l)




