# fonksiyon tanımları ve kullanımı
def veriyaz(ad, adres): # fonksiyon tanımı
    print("Ad: ", ad)
    print("Adres: ", adres)
    return

veriyaz(ad="altan", adres="ankara") # fonksiyon çağrılıyor
# aynı fonksiyon ikinci kez farklı paramtere değeri ve sıralaması ile çağrılıyor
veriyaz(adres="istanbul", ad="emel") # parametre sıralaması farketmez
                                       

          
